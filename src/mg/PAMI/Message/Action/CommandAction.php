class CommandAction extends ActionMessage
{
    /**
     * Constructor.
     *
     * @param string $command CLI Command to issue.
     * 
     * @return void
     */
    public function __construct($command)
    {
        parent::__construct('Command');
        $this->setKey('Command', $command);
    }
}